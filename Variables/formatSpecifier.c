#include <stdio.h>

int main() {

    // First declare the variable
    int myNum = 15;
    float myFloatNum = 19.99;
    char myChar = 'B';

    // Then using format specifier print it;
    printf("My whole number:%d\n", myNum);
    printf("My float number:%f\n", myFloatNum);
    printf("My char:%c\n", myChar);
    return 0;
}

